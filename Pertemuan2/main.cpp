#include <iostream>

using namespace std;

int main()
{
   int a,b;
   float x,y;
   x = 19;
   y = 18;
   a = 19;
   b = 18;

   cout << "\t a + b = " << (a + b) << endl;
   cout << "a - b = " << (a - b) << endl;
   cout << "a * b = " << (a * b) << endl;
   cout << "a / b = " << (x / y) << endl;
   cout << "a % b = " << (a % b) << endl;


    return 0;
}
